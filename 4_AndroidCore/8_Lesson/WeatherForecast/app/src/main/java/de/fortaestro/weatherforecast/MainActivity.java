package de.fortaestro.weatherforecast;

import android.os.Build;
import android.os.Handler;
import android.support.annotation.RequiresApi;
import android.support.v7.app.AppCompatActivity;
import android.os.Bundle;
import android.util.Log;
import android.view.View;
import android.widget.Button;
import android.widget.EditText;

import com.google.gson.Gson;

import java.io.BufferedReader;
import java.io.InputStreamReader;
import java.net.MalformedURLException;
import java.net.URL;
import java.util.stream.Collectors;

import javax.net.ssl.HttpsURLConnection;

import de.fortaestro.weatherforecast.model.WeatherRequest;

public class MainActivity extends AppCompatActivity
{
    private static final String TAG = "WEATHER";
    private static final String WEATHER_URL =
            //"https://api.openweathermap.org/data/2.5/weather?q=Schwarzenbek,DE&appid=";
            "https://api.openweathermap.org/data/2.5/weather?lat=55.75&lon=37.62&appid=";
    private static final String WEATHER_API_KEY = "5c6ff8960ab6bef5a401813f57297807";
    private EditText city;
    private EditText temperature;
    private EditText pressure;
    private EditText humidity;
    private EditText windSpeed;

    @Override
    protected void onCreate(Bundle savedInstanceState)
    {
        super.onCreate(savedInstanceState);

        setContentView(R.layout.activity_main);
        init();
    }

    private void init()
    {
        city = findViewById(R.id.textCity);
        temperature = findViewById(R.id.textTemprature);
        pressure = findViewById(R.id.textPressure);
        humidity = findViewById(R.id.textHumidity);
        windSpeed = findViewById(R.id.textWindspeed);

        Button refresh = findViewById(R.id.refresh);
        refresh.setOnClickListener(clickListener);
    }

    View.OnClickListener clickListener = new View.OnClickListener()
    {
        @Override
        public void onClick(View v)
        {
            try
            {
                final URL uri = new URL(WEATHER_URL + WEATHER_API_KEY);
                final Handler handler = new Handler(); // Запоминаем основной поток

                new Thread(new Runnable()
                {
                    @RequiresApi(api = Build.VERSION_CODES.N)
                    public void run()
                    {
                        HttpsURLConnection urlConnection = null;

                        try
                        {
                            urlConnection = (HttpsURLConnection)uri.openConnection();
                            urlConnection.setRequestMethod("GET"); // установка метода получения данных -GET
                            urlConnection.setReadTimeout(10000); // установка таймаута - 10 000 миллисекунд
                            BufferedReader in = new BufferedReader(new InputStreamReader(urlConnection.getInputStream())); // читаем данные в поток
                            String result = getLines(in);
                            System.out.println(result);
                            // преобразование данных запроса в модель
                            Gson gson = new Gson();
                            final WeatherRequest weatherRequest = gson.fromJson(result, WeatherRequest.class);

                            // Возвращаемся к основному потоку
                            handler.post(new Runnable()
                            {
                                @Override
                                public void run()
                                {
                                    displayWeather(weatherRequest);
                                }
                            });
                        }
                        catch (Exception e)
                        {
                            Log.e(TAG, "Fail connection", e);
                            e.printStackTrace();
                        }
                        finally
                        {
                            if (null != urlConnection)
                            {
                                urlConnection.disconnect();
                            }
                        }
                    }
                }).start();
            }
            catch (MalformedURLException e)
            {
                Log.e(TAG, "Fail URI", e);
                e.printStackTrace();
            }
        }

        @RequiresApi(api = Build.VERSION_CODES.N)
        private String getLines(BufferedReader in)
        {
            return in.lines().collect(Collectors.joining (" \n "));
        }

        private void displayWeather(WeatherRequest weatherRequest)
        {
            city.setText(weatherRequest.getName());
            temperature.setText(String.format("%d", weatherRequest.getMain().getTempCelsius()));
            pressure.setText(String.format("%d",  weatherRequest.getMain().getPressure()));
            humidity.setText(String.format("%d", weatherRequest.getMain().getHumidity()));
            windSpeed.setText(String.format("%f2", weatherRequest.getWind().getSpeed()));
        }
    };
}